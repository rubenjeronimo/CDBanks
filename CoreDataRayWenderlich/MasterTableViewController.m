@implementation MasterTableViewController

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    NSFetchRequest *consultaTabla = [[NSFetchRequest alloc]init];
    NSEntityDescription *entidad = [NSEntityDescription entityForName:@"FailedBankInfo" inManagedObjectContext:self.contexto];
    [consultaTabla setEntity:entidad];
    NSError *error;
    self.listadoBancos = [self.contexto executeFetchRequest:consultaTabla error:&error];
    self.title = @"bancos del parque";
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{

    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{

    // Return the number of rows in the section.
    return [self.listadoBancos count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *cellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
    FailedBankInfo *infoBanco = [self.listadoBancos objectAtIndex:indexPath.row];
    cell.textLabel.text = infoBanco.name;
    cell.detailTextLabel.text = [NSString stringWithFormat: @"%@ %@", infoBanco.ciudad, infoBanco.estado];
    return cell;
}


